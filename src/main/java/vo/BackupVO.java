package vo;

public class BackupVO {
	private String backup_user_name;
	private String backup_user_id;
	private String backup_user_passwd;
	private String backup_user_birth;
	private String backup_date;
	
	
	public String getBackup_user_name() {
		return backup_user_name;
	}
	public void setBackup_user_name(String backup_user_name) {
		this.backup_user_name = backup_user_name;
	}
	public String getBackup_user_id() {
		return backup_user_id;
	}
	public void setBackup_user_id(String backup_user_id) {
		this.backup_user_id = backup_user_id;
	}
	public String getBackup_user_passwd() {
		return backup_user_passwd;
	}
	public void setBackup_user_passwd(String backup_user_passwd) {
		this.backup_user_passwd = backup_user_passwd;
	}
	public String getBackup_user_birth() {
		return backup_user_birth;
	}
	public void setBackup_user_birth(String backup_user_birth) {
		this.backup_user_birth = backup_user_birth;
	}
	public String getBackup_date() {
		return backup_date;
	}
	public void setBackup_date(String backup_date) {
		this.backup_date = backup_date;
	}
}
